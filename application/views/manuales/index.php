      <div class="row text-center">
        <h1 class="title-pasos" style="color: #009966;">Procedimiento para validar Profesores que ingresan a Superprofe.co</h1>
        <h3 class="title-pasos">
          Cada profesor que se registra en Super Profe debe pasar por un riguroso proceso de
          validación. A continuación se describen los pasos de este proceso:
        </h3>
      </div>
      <div class="row center-block pasos" style="margin-top:50px;">
        <div class="col-md-6 col-md-offset-3">
            <h1>1.</h1>
            <h4>Verificación de antecedentes judiciales:</h4>
            <p>
              Verificación de antecedentes judiciales: Se realizará la revisión de los antecedentes judiciales de cada profesor a través de la página: <a style="word-break: break-all;"href="https://antecedentes.policia.gov.co:7005/WebJudicial/index.xhtml">https://antecedentes.policia.gov.co:7005/WebJudicial/index.xhtml</a>
            </p>    
        </div>
      </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>2.</h1>
              <h4>Verificación de referencia personal:</h4>
              <p>
                Cada Profesor suministrará una referencia personal a la que compañia llamará a pedir referencias.
              </p>    
          </div>
        </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>3.</h1>
              <h4>Verificación de referencia laboral: </h4>
              <p>
                Cada Profesor suministrará una referencia profesional a la que compañia llamará a pedir referencias.
              </p>    
          </div>
        </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>4.</h1>
              <h4>Certificados</h4>
              <p>
              Cada aplicante debe traer a la entrevista los siguientes certificados, si el aplicante los tiene digitales puede enviarlos al correo <a href="mailto:hola@superprofe.co" target="_top">hola@superprofe.co</a>:<br>
              <br>
              <br>
              </p>

              <p>
              -Certificado de estudios, en caso de que el aplicante sea un estudiante debe anexar
              un certificado de notas del último semestre cursado.
              <br>
              <br>
              - Certificados laborales.
              <br>
              <br>
              - Copia de la cédula.
              </p> 
              <br>   
          </div>
        </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>5.</h1>
              <h4>Verificación de referencia laboral: </h4>
              <p>
                Cada aplicante debe responder antes de la entrevista el siguiente test de personalidad <a href="http://www.16personalities.com/es/test-de-personalidad" target="_blank">http://www.16personalities.com/es/test-de-personalidad</a> y traer los resultados impresos.
              </p>    
          </div>
        </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>6.</h1>
              <h4>Enlaces</h4>
              <p>
              Se recomienda que revisen los siguientes enlaces:<br><br>
              <a href="https://www.youtube.com/watch?v=x97biL1lKBE" target="_blank">https://www.youtube.com/watch?v=x97biL1lKBE</a><br>
              <a href="http://superprofe.co/terminos" target="_blank">http://superprofe.co/terminos</a>
              </p>    
          </div>
        </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>5.</h1>
              <h4>Pagos</h4>
              <p>
                 El valor promedio que se paga por hora de clase oscila entre 20.000 y 50.000 y depende de la experiencia del profesor y nivel de las áreas que que se van a dictar. Estos valores serán acordados en la entrevista. 
              </p>    
          </div>
        </div>

        <div class="row center-block pasos" style="margin-top:50px;">
          <div class="col-md-6 col-md-offset-3">
              <h1>5.</h1>
              <h4>Contrato</h4>
              <p>
                 Por último si el aplicante cumple con todos los requisitos al terminar la entrevista se procederá a firmar el contrato para pertenecer a la comunidad de Súper Héroes del Conocimiento.
              </p>    
          </div>
